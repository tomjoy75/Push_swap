#include "push_swap.h"

void	rotate(t_stack **stack)
{
	t_stack	*temp;

	if (node_nb(*stack) < 2)
		return ;
	temp = (*stack)->next;
	find_last_elem(*stack)->next = *stack;
	(*stack)->next = NULL;
	*stack = temp;
	position_update(*stack);
}

void	ra(t_stack **a)
{
	rotate(a);
	ft_printf("ra\n");
}

void	rb(t_stack **b)
{
	rotate(b);
	ft_printf("rb\n");
}

void	rr(t_stack **a, t_stack **b)
{
	rotate(a);
	rotate(b);
	ft_printf("rr\n");
}
